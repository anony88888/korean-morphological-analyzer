/* 수사 표제어 인덱스 구조체 */
/* 앞 글자를 기준으로 인덱스를 지정 */
typedef struct {
	H_CHAR fChar;						/* 완성형 가장 앞글자 */
	DWORD idx;							/* 표제어 버퍼 인덱스 */
	UWORD num;							/* 같은 글자로 시작되는 수사의 개수 */
} tSUSAINDEX;

/* 수사 표제어 구조체 */
/*
	< 수사 정보 >
	101 : 한자표기 수사 (일,이,삼,...,십)
	102 : 한글표기 수사 (하나,둘,셋,...,열,스물,...,아흔)
	103 : 불완전형 한글표기 수사 (한, 두, 세, 네)
		  (단위명사나 일반명사가 올 수 있다.)
	104 : 단위명사 결합형
	105 : 바로 뒤에 항상 단위전치어/단위명사가 오는 수사(마지막 글자에 종성이 있는 경우)
	106 : '수'가 앞에 붙어서 불특정 수/양을 나타내는 수사 
	107 : 백,천,만,억(1,2와 같이 결합하여 사용)
*/
typedef struct {
	UBYTE  Str[SUSAWORDLEN];			/* 완성형 수사 */
	H_CHAR wStr[SUSAWORDLEN];			/* 완성형 수사 : 내부 버퍼 포멧 */
	HANGUL jStr[SUSAWORDLEN];			/* 조합형 수사 */
	UWORD len;							/* 길이 */
	BYTE num[SUSAWORDLEN];				/* 한글 수사에 대한 숫자 표현 */
	WORD info;							/* 수사 사전 정보 */
} tSUSAWORD;

/* 수사 형태소 저장 구조체 */
typedef struct {
	UBYTE Str[SUSAWORDLEN];				/* 분석된 수사 형태소 */
	UWORD mode;							/* 수사 토큰의 종류 */
} tSTATSUSA;

/* 단위명사 저장 구조체 */
typedef struct {
	UBYTE  Str[SUSAWORDLEN];			/* 완성형 단위명사 */
	H_CHAR wStr[SUSAWORDLEN];			/* 완성형 단위명사 : 내부 버퍼 포멧 */
	HANGUL jStr[SUSAWORDLEN];			/* 조합형 단위명사 */
	UWORD len;							/* 길이 */
	UWORD idx;					/* 만일 단위명사+접미사로 구성되었을 경우... 접미사의 시작위치 */
} tUNITNOUN;


